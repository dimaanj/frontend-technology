/**
 * Даны два отсортированных списка интервалов присутствия пользователей онлайн в течение дня.
 * Каждый интервал задаётся как [start, end], где 0 <= start < end <= 24.
 *
 * Нужно вычислить интервалы, когда оба пользователя были онлайн одновременно.
 *
 * Примеры (можешь подогнать под своё точное условие формата):
 *
 * intersection(
 *   [[8, 12], [17, 22]],
 *   [[9, 11], [14, 18], [20, 23]]
 * ); // ожидаем: [[9, 11], [17, 18], [20, 22]]
 *
 * intersection(
 *   [[9, 15], [18, 21]],
 *   [[10, 14], [21, 22]]
 * ); // ожидаем: [[10, 14]]
 *
 * @param {number[][]} user1 - отсортированный по start список интервалов первого пользователя
 * @param {number[][]} user2 - отсортированный по start список интервалов второго пользователя
 * @returns {number[][]} - список интервалов пересечения
 */
export default function intersection(user1, user2) {
  // Уточни для себя (важно для реализации и тестов):
  // - Интервалы считаем полуоткрытыми [start, end) или закрытыми [start, end]?
  // - Входные интервалы у каждого пользователя не пересекаются между собой?

  let i = 0;
  let j = 0;
  const res = [];

  while(i < user1.length && j < user2.length) {
    const current1 = user1[i];
    const current2 = user2[j];

    const start = Math.max(current1[0], current2[0]);
    const end = Math.min(current1[1], current2[1]);

    if(start < end) {
        res.push([start, end]);
    }

    if(current1[1] < current2[1]) {
        i++;
    } else {
        j++;
    }
  }

  return res;
}

